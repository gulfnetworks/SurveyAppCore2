        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> EditUser(UserDetailViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }
            var user = await _userManager.FindByEmailAsync(model.Email);
            if (user == null)
            {
                // Don't reveal that the user does not exist
                return RedirectToAction(nameof(ResetPasswordConfirmation));
            }
            var token = _userManager.GeneratePasswordResetTokenAsync(user).Result;

            var result = await _userManager.ResetPasswordAsync(user, token, model.Password);
            if (result.Succeeded)
            {
                UserDetailDataProvider userDetailDataProvider = new UserDetailDataProvider();

                UserDetail userDetail = new UserDetail();
                userDetail.Active = model.Active;
                userDetail.UserDetailId = model.UserDetailId;
                userDetail.UserFirstName = model.UserFirstName;
                userDetail.UserLastName = model.UserLastName;
                userDetail.MobileNo = model.MobileNo;
                userDetail.UserEmail = model.Email;
                userDetail.JobTitleId = model.JobTitleId;
                userDetail.ManagerId = model.ManagerId;
                userDetail.OutletId = model.OutletId;
                userDetail.ExtraOutletId = model.ExtraOutletId;
                userDetail.SubscriptionId = model.SubscriptionId;
                userDetail.SettingsId = model.SettingsId;
       

                await userDetailDataProvider.UpdateUserDetail(userDetail);


                return RedirectToAction(nameof(UserList));
            }
            AddErrors(result);
            return View();
        }



